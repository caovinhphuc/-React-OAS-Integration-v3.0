import React, { useCallback, useEffect, useState } from 'react';
import { useNavigate } from 'react-router-dom';
import AuthService from '../../services/authService';
import styles from './LoginForm.module.scss';

const LoginForm: React.FC = () => {
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [rememberMe, setRememberMe] = useState(false);
  const [showPassword, setShowPassword] = useState(false);
  const [isLoading, setIsLoading] = useState(false);
  const [errors, setErrors] = useState<{
    email?: string;
    password?: string;
    general?: string;
  }>({});
  const [touched, setTouched] = useState<{
    email?: boolean;
    password?: boolean;
  }>({});
  const [loginAttempts, setLoginAttempts] = useState(0);
  const [isAccountLocked, setIsAccountLocked] = useState(false);

  const navigate = useNavigate();

  // Check for existing lockout
  useEffect(() => {
    const lockoutTime = localStorage.getItem('lockoutTime');
    const attempts = localStorage.getItem('loginAttempts');

    if (lockoutTime && attempts) {
      const lockoutTimestamp = parseInt(lockoutTime);
      const attemptCount = parseInt(attempts);

      if (Date.now() < lockoutTimestamp) {
        setIsAccountLocked(true);
        setLoginAttempts(attemptCount);
      } else {
        localStorage.removeItem('lockoutTime');
        localStorage.removeItem('loginAttempts');
      }
    } else if (attempts) {
      setLoginAttempts(parseInt(attempts));
    }
  }, []);

  const validateForm = useCallback(() => {
    const newErrors: { email?: string; password?: string; general?: string } =
      {};

    if (!email) {
      newErrors.email = 'Email l√† b·∫Øt bu·ªôc';
    } else if (!/\S+@\S+\.\S/.test(email)) {
      newErrors.email = 'Email kh√¥ng h·ª£p l·ªá';
    }

    if (!password) {
      newErrors.password = 'M·∫≠t kh·∫©u l√† b·∫Øt bu·ªôc';
    } else if (password.length < 6) {
      newErrors.password = 'M·∫≠t kh·∫©u ph·∫£i c√≥ √≠t nh·∫•t 6 k√Ω t·ª±';
    }

    setErrors(newErrors);
    return Object.keys(newErrors).length === 0;
  }, [email, password]);

  const handleLogin = useCallback(async () => {
    if (!validateForm()) {
      return;
    }
    setIsLoading(true);
    setErrors({});

    try {
      console.log('üîê Attempting login with:', {
        email,
        password: '***',
        rememberMe,
      });
      console.log('üîê Calling AuthService.login...');
      const result = await AuthService.login(email, password, rememberMe);
      console.log('üîê Login result:', result);
      console.log('üîê Result success:', result.success);
      console.log('üîê Result user:', result.user);

      if (result.success && result.user) {
        console.log('‚úÖ Login successful, navigating to dashboard');
        console.log('‚úÖ User data:', result.user);
        console.log('‚úÖ Token:', result.token);
        localStorage.removeItem('loginAttempts');
        localStorage.removeItem('lockoutTime');

        // Force SecurityContext to check session again
        setTimeout(() => {
          console.log('üöÄ About to navigate to /');
          navigate('/', { replace: true });
          console.log('üöÄ Navigation to / called');
        }, 100);
      } else {
        console.log('‚ùå Login failed:', result.message);
        console.log('‚ùå Result object:', result);
        const newAttempts = loginAttempts + 1;
        setLoginAttempts(newAttempts);
        localStorage.setItem('loginAttempts', newAttempts.toString());

        if (newAttempts >= 5) {
          const lockoutTime = Date.now() + 15 * 60 * 1000;
          localStorage.setItem('lockoutTime', lockoutTime.toString());
          setIsAccountLocked(true);
          setErrors({
            general:
              'Qu√° nhi·ªÅu l·∫ßn ƒëƒÉng nh·∫≠p th·∫•t b·∫°i. T√†i kho·∫£n ƒë√£ b·ªã kh√≥a 15 ph√∫t.',
          });
        } else {
          setErrors({
            general:
              result.message ||
              `Th√¥ng tin ƒëƒÉng nh·∫≠p kh√¥ng ch√≠nh x√°c. C√≤n ${5 - newAttempts} l·∫ßn th·ª≠.`,
          });
        }
      }
    } catch (error) {
      console.error('Login error:', error);
      setErrors({ general: 'C√≥ l·ªói x·∫£y ra. Vui l√≤ng th·ª≠ l·∫°i sau.' });
    } finally {
      setIsLoading(false);
    }
  }, [email, password, rememberMe, loginAttempts, navigate, validateForm]);

  const handleSubmit = (e: React.FormEvent) => {
    e.preventDefault();
    handleLogin();
  };

  const handleEmailChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    setEmail(e.target.value);
    setTouched({ ...touched, email: true });
    if (errors.email) {
      setErrors({ ...errors, email: undefined });
    }
  };

  const handlePasswordChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    setPassword(e.target.value);
    setTouched({ ...touched, password: true });
    if (errors.password) {
      setErrors({ ...errors, password: undefined });
    }
  };

  const clearForm = () => {
    setEmail('');
    setPassword('');
    setErrors({});
    setTouched({});
  };

  return (
    <div className={styles.loginForm}>
      <div className={styles.header}>
        <div className={styles.logo}>
          <h1>üè¢ MIA.vn</h1>
        </div>
        <h2>Ch√†o m·ª´ng tr·ªü l·∫°i!</h2>
        <p>ƒêƒÉng nh·∫≠p ƒë·ªÉ ti·∫øp t·ª•c qu·∫£n l√Ω doanh nghi·ªáp c·ªßa b·∫°n</p>

        {/* Debug Buttons - Temporary for development */}
        <div style={{ display: 'flex', gap: '10px', marginTop: '10px' }}>
          <button
            type="button"
            onClick={() => {
              localStorage.removeItem('lockoutTime');
              localStorage.removeItem('loginAttempts');
              setIsAccountLocked(false);
              setLoginAttempts(0);
              setErrors({});
              alert('ƒê√£ x√≥a t·∫•t c·∫£ d·ªØ li·ªáu lockout!');
            }}
            style={{
              padding: '0.5rem 1rem',
              background: '#ef4444',
              color: 'white',
              border: 'none',
              borderRadius: '6px',
              fontSize: '0.8rem',
              cursor: 'pointer',
            }}
          >
            üßπ Clear Lockout
          </button>

          <button
            type="button"
            onClick={async () => {
              try {
                const response = await fetch('/api/auth/roles');
                const data = await response.json();
                console.log('Backend test result:', {
                  status: response.status,
                  data,
                });
                alert(
                  `Backend test: ${response.status === 200 ? 'OK' : 'ERROR'}\nStatus: ${response.status}\nData: ${JSON.stringify(data).substring(0, 100)}...`
                );
              } catch (error) {
                console.error('Backend test error:', error);
                alert(`Backend test ERROR: ${error.message}`);
              }
            }}
            style={{
              padding: '0.5rem 1rem',
              background: '#3b82f6',
              color: 'white',
              border: 'none',
              borderRadius: '6px',
              fontSize: '0.8rem',
              cursor: 'pointer',
            }}
          >
            üîó Test Backend
          </button>
        </div>
      </div>

      {/* General Error Message */}
      {errors.general && (
        <div className={styles.generalError}>
          ‚ùå<span>{errors.general}</span>
        </div>
      )}

      {/* Account Locked Warning */}
      {isAccountLocked && (
        <div className={styles.lockoutWarning}>
          ‚ö†Ô∏è
          <span>
            T√†i kho·∫£n t·∫°m th·ªùi b·ªã kh√≥a v√¨ qu√° nhi·ªÅu l·∫ßn ƒëƒÉng nh·∫≠p th·∫•t b·∫°i
          </span>
          <button
            type="button"
            className={styles.unlockButton}
            onClick={() => {
              // Clear lockout data
              localStorage.removeItem('lockoutTime');
              localStorage.removeItem('loginAttempts');
              setIsAccountLocked(false);
              setLoginAttempts(0);
              setErrors({});
              alert('T√†i kho·∫£n ƒë√£ ƒë∆∞·ª£c m·ªü kh√≥a! B·∫°n c√≥ th·ªÉ ƒëƒÉng nh·∫≠p l·∫°i.');
            }}
          >
            üîì M·ªü kh√≥a t√†i kho·∫£n
          </button>
        </div>
      )}

      <form onSubmit={handleSubmit} className={styles.form} noValidate>
        <div className={styles.formGroup}>
          <label htmlFor="email">üë§ Email</label>
          <input
            id="email"
            type="email"
            value={email}
            onChange={handleEmailChange}
            onBlur={() => setTouched({ ...touched, email: true })}
            placeholder="Nh·∫≠p email c·ªßa b·∫°n"
            disabled={isLoading || isAccountLocked}
            className={errors.email && touched.email ? styles.error : ''}
            aria-describedby={errors.email ? 'email-error' : undefined}
          />
          {errors.email && touched.email && (
            <span
              id="email-error"
              className={styles.errorMessage}
              role="alert"
              aria-live="polite"
            >
              ‚ö†Ô∏è {errors.email}
            </span>
          )}
        </div>

        <div className={styles.formGroup}>
          <label htmlFor="password">üîí M·∫≠t kh·∫©u</label>
          <div className={styles.passwordContainer}>
            <input
              id="password"
              type={showPassword ? 'text' : 'password'}
              value={password}
              onChange={handlePasswordChange}
              onBlur={() => setTouched({ ...touched, password: true })}
              placeholder="Nh·∫≠p m·∫≠t kh·∫©u"
              disabled={isLoading || isAccountLocked}
              className={
                errors.password && touched.password ? styles.error : ''
              }
              aria-describedby={errors.password ? 'password-error' : undefined}
            />
            <button
              type="button"
              className={styles.passwordToggle}
              onClick={() => setShowPassword(!showPassword)}
              title={showPassword ? '·∫®n m·∫≠t kh·∫©u' : 'Hi·ªán m·∫≠t kh·∫©u'}
              aria-label={showPassword ? '·∫®n m·∫≠t kh·∫©u' : 'Hi·ªán m·∫≠t kh·∫©u'}
              disabled={isLoading || isAccountLocked}
            >
              {showPassword ? 'üôà' : 'üëÅÔ∏è'}
            </button>
          </div>
          {errors.password && touched.password && (
            <span
              id="password-error"
              className={styles.errorMessage}
              role="alert"
              aria-live="polite"
            >
              ‚ö†Ô∏è {errors.password}
            </span>
          )}
        </div>

        <div className={styles.checkbox}>
          <input
            type="checkbox"
            id="remember"
            checked={rememberMe}
            onChange={(e) => setRememberMe(e.target.checked)}
            disabled={isLoading || isAccountLocked}
          />
          <label htmlFor="remember">Ghi nh·ªõ ƒëƒÉng nh·∫≠p</label>
          <a href="#" className={styles.forgotPassword}>
            Qu√™n m·∫≠t kh·∫©u?
          </a>
        </div>

        <div className={styles.quickActions}>
          <button
            type="submit"
            className={styles.loginButton}
            disabled={isLoading || isAccountLocked}
            data-loading={isLoading}
          >
            {isLoading ? 'ƒêang ƒëƒÉng nh·∫≠p...' : 'ƒêƒÉng Nh·∫≠p'}
          </button>
          <button
            type="button"
            className={styles.clearButton}
            onClick={clearForm}
            disabled={isLoading || isAccountLocked}
            title="X√≥a form"
          >
            ‚ùå X√≥a
          </button>
        </div>

        <div className={styles.footer}>
          <p>
            Ch∆∞a c√≥ t√†i kho·∫£n? <a href="#">ƒêƒÉng k√Ω ngay</a>
          </p>
          <p className={styles.securityNote}>
            üîí B·∫£o m·∫≠t v·ªõi m√£ h√≥a SSL 256-bit
          </p>
        </div>
      </form>
    </div>
  );
};

export default LoginForm;
