# Functions
check_port() {
    local port=$1
    if lsof -Pi :$port -sTCP:LISTEN -t >/dev/null 2>&1; then
        return 0  # Port is in use
    else
        return 1  # Port is free
    fi
}

kill_service() {
    local port=$1
    local service_name=$2
    if check_port $port; then
        local pid=$(lsof -ti:$port)
        warn "D·ª´ng $service_name tr√™n port $port (PID: $pid)"
        kill -9 $pid 2>/dev/null || true
        sleep 2
    fi
}

wait_for_service() {
    local url=$1
    local service_name=$2
    local max_attempts=30
    local attempt=1

    info "Ch·ªù $service_name kh·ªüi ƒë·ªông t·∫°i $url"

    while [ $attempt -le $max_attempts ]; do
        if curl -f -s "$url" > /dev/null 2>&1; then
            log "$service_name ƒë√£ s·∫µn s√†ng!"
            return 0
        fi

        printf "."
        sleep 2
        ((attempt++))
    done

    error "$service_name kh√¥ng th·ªÉ kh·ªüi ƒë·ªông sau $max_attempts l·∫ßn th·ª≠"
}

